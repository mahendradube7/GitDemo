package StringProgram_Practise;

public class P04_LowerUpperCase {

	public static void main(String[] args) {
		
        String str = "VIRAT KOHALI";
		str = str.toLowerCase();
		System.out.println("Lower Case :- " + str);
        
		String str1 = "ROHIT SHARMA";
		str1 = str1.toUpperCase();
		System.out.println("Upper Case :- " +str1);

	}

}
